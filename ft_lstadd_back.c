#include "libft.h"
#include <stdio.h>

void	ft_lstadd_back(t_list **lst, t_list *new);

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	**tracer = lst;
	while (*tracer != NULL)
		tracer = &(*tracer)->next;
	*tracer = new;
}
